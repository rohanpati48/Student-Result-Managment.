    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String course=(String)jComboBox1.getSelectedItem();
        String branch=(String)jComboBox2.getSelectedItem();
        String roll=(String)jTextField1.getText();
        String name=(String)jTextField2.getText();
        String gender=(String)jComboBox3.getSelectedItem();
        String fathername=(String)jTextField3.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/students","root","");
            
            Statement st=con.createStatement();
            st.executeUpdate("insert into student(rollno,course,branchName,name,gender,fathername) values('"+roll+"','"+course+"','"+branch+"','"+name+"','"+gender+"','"+fathername+"')");
            JOptionPane.showMessageDialog(null,"Data Stored Succesfully");
            setVisible(false);
            new adminHome().setVisible(true);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
                
    }//GEN-LAST:event_jButton6ActionPerformed

